import { cold } from 'jasmine-marbles';
import * as _ from 'lodash';
import { of } from 'rxjs';
import { initialState } from '../hearing.test.data';
import { State } from '../store/reducers';
import { LinkedHearingsExclusionHiddenConverter } from './linked-hearings-exclusion.hidden.converter';

describe('LinkedHearingsExclusionHiddenConverter', () => {
  let linkedHearingsExclusionHiddenConverter: LinkedHearingsExclusionHiddenConverter;

  beforeEach(() => {
    linkedHearingsExclusionHiddenConverter = new LinkedHearingsExclusionHiddenConverter();
  });

  it('should transform hidden of true answer', () => {
    const STATE: State = _.cloneDeep(initialState.hearings);
    STATE.hearingValues.serviceHearingValuesModel.screenFlow = null;
    const result$ = linkedHearingsExclusionHiddenConverter.transformHidden(of(STATE));
    const expected = cold('(b|)', { b: true });
    expect(result$).toBeObservable(expected);
  });

  it('should transform hidden of false answer', () => {
    const STATE: State = _.cloneDeep(initialState.hearings);
    const result$ = linkedHearingsExclusionHiddenConverter.transformHidden(of(STATE));
    const expected = cold('(b|)', { b: false });
    expect(result$).toBeObservable(expected);
  });
});
